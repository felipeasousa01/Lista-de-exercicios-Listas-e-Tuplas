# Sistema de Locadora de Filmes Online 

# Lista de filmes dispon√≠veis
filmes = [
    {"id": 1, "titulo": "Vingadores: Ultimato", "disponivel": True},
    {"id": 2, "titulo": "Harry Potter e a Pedra Filosofal", "disponivel": True},
    {"id": 3, "titulo": "O Senhor dos An√©is: A Sociedade do Anel", "disponivel": True},
    {"id": 4, "titulo": "Matrix", "disponivel": True},
    {"id": 5, "titulo": "Titanic", "disponivel": True}
]

# Lista de loca√ß√µes
locacoes = []

# Loop principal do sistema
while True:
    print("\nüé¨ LOCADORA DE FILMES ONLINE üé¨")
    print("1 - Listar filmes")
    print("2 - Locar filme")
    print("3 - Devolver filme")
    print("4 - Encerrar sistema")
    opcao = input("Escolha uma op√ß√£o: ")

    # Listar filmes
    if opcao == "1":
        print("\nüìΩÔ∏è Lista de Filmes:")
        for filme in filmes:
            if filme["disponivel"]:
                status = "Dispon√≠vel"
            else:
                status = "Indispon√≠vel"
            print(f"{filme['id']} - {filme['titulo']} ({status})")

    # Locar filme
    elif opcao == "2":
        print("\nüìΩÔ∏è Lista de Filmes para loca√ß√£o:")
        for filme in filmes:
            status = "Dispon√≠vel" if filme["disponivel"] else "Indispon√≠vel"
            print(f"{filme['id']} - {filme['titulo']} ({status})")

        escolha = int(input("\nDigite o ID do filme que deseja locar: "))
        encontrado = False
        for filme in filmes:
            if filme["id"] == escolha:
                encontrado = True
                if filme["disponivel"]:
                    filme["disponivel"] = False
                    dias = int(input("Por quantos dias deseja alugar? "))
                    locacoes.append({"id_filme": filme["id"], "dias": dias})
                    print(f"‚úÖ Voc√™ locou '{filme['titulo']}' por {dias} dias.")
                else:
                    print("‚ö†Ô∏è Filme indispon√≠vel no momento.")
        if not encontrado:
            print("‚ö†Ô∏è Filme n√£o encontrado.")

    # Devolver filme
    elif opcao == "3":
        if len(locacoes) == 0:
            print("\n‚ö†Ô∏è Nenhum filme locado no momento.")
        else:
            print("\nFilmes locados:")
            for loc in locacoes:
                for filme in filmes:
                    if filme["id"] == loc["id_filme"]:
                        print(f"{filme['id']} - {filme['titulo']} (Dias alugados: {loc['dias']})")

            escolha = int(input("\nDigite o ID do filme que deseja devolver: "))
            devolvido = False
            for loc in locacoes:
                if loc["id_filme"] == escolha:
                    for filme in filmes:
                        if filme["id"] == escolha:
                            filme["disponivel"] = True
                            atraso = int(input("Quantos dias de atraso na devolu√ß√£o? "))
                            if atraso > 0:
                                multa = atraso * 2.50
                                print(f"‚ö†Ô∏è Devolu√ß√£o com {atraso} dias de atraso. Multa: R${multa:.2f}")
                            else:
                                print("‚úÖ Filme devolvido sem atraso.")
                            locacoes.remove(loc)
                            devolvido = True
                            break
            if not devolvido:
                print("‚ö†Ô∏è Filme n√£o encontrado na lista de loca√ß√µes.")

    # Encerrar sistema
    elif opcao == "4":
        print("üëã Sistema encerrado. Obrigado por usar a Locadora Online!")
        break

    else:
        print("‚ö†Ô∏è Op√ß√£o inv√°lida. Tente novamente.")
